class Treatment:
    def __init__(self, id, surgery, surgeryDate, medic, medicStart, medicFinish, abuse, abandon):
        self.id = id
        self.surgery = surgery
        self.surgeryDate = surgeryDate
        self.medic = medic
        self.medicStart = medicStart
        self.medicFinish = medicFinish
        self.abuse = abuse
        self.abandon = abandon